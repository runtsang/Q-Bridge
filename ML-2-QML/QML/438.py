class QuantumHybridTransformerBlock(TransformerBlockBase):
    """Hybrid block that uses quantum attention and optionally quantum feed‑forward."""
    def __init__(self, embed_dim: int, num_heads: int, ffn_dim: int,
                 dropout: float = 0.1, use_quantum_ffn: bool = False,
                 n_qubits_ffn: int = 0, q_device: Optional[tq.QuantumDevice] = None) -> None:
        super().__init__(embed_dim, num_heads, dropout)
        self.attn = MultiHeadAttentionQuantum(embed_dim, num_heads, dropout, q_device=q_device)
        if use_quantum_ffn and n_qubits_ffn > 0:
            self.ffn = FeedForwardQuantum(embed_dim, ffn_dim, n_qubits_ffn, dropout)
        else:
            self.ffn = FeedForwardClassical(embed_dim, ffn_dim, dropout)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        attn_out = self.attn(x)
        x = self.norm1(x + self.dropout(attn_out))
        ffn_out = self.ffn(x)
        return self.norm2(x + self.dropout(ffn_out))


class QuantumEnsembleClassifier(nn.Module):
    """Ensemble of quantum‑enhanced classifiers with learnable weights."""
    def __init__(self, classifiers: List[nn.Module], init_weights: Optional[List[float]] = None) -> None:
        super().__init__()
        if not classifiers:
            raise ValueError("At least one classifier is required")
        self.classifiers = nn.ModuleList(classifiers)
        if init_weights is None:
            init_weights = [1.0] * len(classifiers)
        if len(init_weights)!= len(classifiers):
            raise ValueError("Length of init_weights must match number of classifiers")
        self.register_parameter(
            "weights",
            nn.Parameter(torch.tensor(init_weights, dtype=torch.float32))
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        logits = [clf(x) for clf in self.classifiers]
        logits = torch.stack(logits, dim=0)  # (n_cls, batch, out)
        weights = F.softmax(self.weights, dim=0).view(-1, 1, 1)
        weighted = logits * weights
        return weighted.sum(dim=0)


