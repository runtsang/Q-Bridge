class GraphQNNGen047:
    """Unified interface for quantum graph‑based neural networks.

    Provides static constructors for the quantum graph neural network,
    the hybrid quantum‑classical classifier, a quantum fully‑connected
    layer, and a photonic fraud‑detection program.  The same class name
    is used in the classical module to expose an identical API.
    """
    QuantumCircuit = QuantumCircuit
    HybridFunction = HybridFunction
    Hybrid = Hybrid
    QCNet = QCNet
    QuantumFCL = QuantumFCL
    FraudLayerParameters = FraudLayerParameters
    build_fraud_detection_program = staticmethod(build_fraud_detection_program)

    @staticmethod
    def random_network(qnn_arch: Sequence[int], samples: int):
        return random_network(qnn_arch, samples)

    @staticmethod
    def feedforward(qnn_arch: Sequence[int], unitaries: Sequence[Sequence[qt.Qobj]],
                    samples: Iterable[Tuple[qt.Qobj, qt.Qobj]]):
        return feedforward(qnn_arch, unitaries, samples)

    @staticmethod
    def fidelity_adjacency(states: Sequence[qt.Qobj], threshold: float,
                           *, secondary: Optional[float] = None,
                           secondary_weight: float = 0.5):
        return fidelity_adjacency(states, threshold,
                                  secondary=secondary,
                                  secondary_weight=secondary_weight)

