class UnifiedTransformerFraudClassifier(nn.Module):
    """
    Quantum‑enhanced transformer‑based text classifier with an optional photonic fraud‑detection head.
    The head can be a classical neural network or a photonic circuit.
    """

    def __init__(self,
                 vocab_size: int,
                 embed_dim: int,
                 num_heads: int,
                 num_blocks: int,
                 ffn_dim: int,
                 num_classes: int,
                 dropout: float = 0.1,
                 n_qubits_transformer: int = 0,
                 n_qubits_ffn: int = 0,
                 n_qlayers: int = 1,
                 q_device: Optional[tq.QuantumDevice] = None,
                 use_fraud_head: bool = False,
                 fraud_head_params: Optional[Tuple[FraudLayerParameters, Iterable[FraudLayerParameters]]] = None):
        super().__init__()
        self.token_embedding = nn.Embedding(vocab_size, embed_dim)
        self.pos_embedding = PositionalEncoder(embed_dim)
        if n_qubits_transformer > 0:
            q_device = q_device or tq.QuantumDevice(n_wires=max(n_qubits_transformer, n_qubits_ffn))
            blocks = [
                TransformerBlockQuantum(
                    embed_dim, num_heads, ffn_dim,
                    n_qubits_transformer, n_qubits_ffn, n_qlayers,
                    q_device=q_device, dropout=dropout
                ) for _ in range(num_blocks)
            ]
        else:
            blocks = [
                TransformerBlockBase(embed_dim, num_heads, dropout)  # fallback to classical
            ]
        self.transformers = nn.Sequential(*blocks)
        self.dropout = nn.Dropout(dropout)

        if use_fraud_head and fraud_head_params is not None:
            input_params, layers = fraud_head_params
            self.fraud_input_proj = nn.Linear(embed_dim, 2)
            self.fraud_head = FraudDetectionHeadQuantum(input_params, layers)
        else:
            self.fraud_head = None
            self.classifier = nn.Linear(embed_dim,
                                       num_classes if num_classes > 2 else 1)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        tokens = self.token_embedding(x)
        x = self.pos_embedding(tokens)
        x = self.transformers(x)
        pooled = x.mean(dim=1)
        if self.fraud_head is not None:
            x = self.fraud_input_proj(pooled)
            x = self.fraud_head(x)
        else:
            x = self.classifier(pooled)
        return x

