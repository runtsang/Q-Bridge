class GraphQNNHybrid:
    """Hybrid interface providing both classical and quantum utilities for graph‑based neural networks,
    regression, fraud detection, and classification.  The static methods mirror the original
    seed modules while adding a unified API that can be used interchangeably in downstream
    pipelines."""
    random_network = staticmethod(random_network)
    feedforward = staticmethod(feedforward)
    fidelity_adjacency = staticmethod(fidelity_adjacency)
    generate_superposition_data = staticmethod(generate_superposition_data)
    RegressionDataset = RegressionDataset
    QModel = QModel
    FraudLayerParameters = FraudLayerParameters
    build_fraud_detection_program = staticmethod(build_fraud_detection_program)
    build_classifier_circuit = staticmethod(build_classifier_circuit)

