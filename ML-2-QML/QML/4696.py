import torch
import torchquantum as tq

class QuanvolutionFilter(tq.QuantumModule):
    """Quantum convolution filter that applies a random two‑qubit kernel to 2×2 patches."""
    def __init__(self):
        super().__init__()
        self.n_wires = 4
        self.encoder = tq.GeneralEncoder(
            [
                {"input_idx": [0], "func": "ry", "wires": [0]},
                {"input_idx": [1], "func": "ry", "wires": [1]},
                {"input_idx": [2], "func": "ry", "wires": [2]},
                {"input_idx": [3], "func": "ry", "wires": [3]},
            ]
        )
        self.q_layer = tq.RandomLayer(n_ops=8, wires=list(range(self.n_wires)))
        self.measure = tq.MeasureAll(tq.PauliZ)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        bsz = x.shape[0]
        device = x.device
        qdev = tq.QuantumDevice(self.n_wires, bsz=bsz, device=device)
        x = x.view(bsz, 28, 28)
        patches = []
        for r in range(0, 28, 2):
            for c in range(0, 28, 2):
                data = torch.stack(
                    [
                        x[:, r, c],
                        x[:, r, c + 1],
                        x[:, r + 1, c],
                        x[:, r + 1, c + 1],
                    ],
                    dim=1,
                )
                self.encoder(qdev, data)
                self.q_layer(qdev)
                measurement = self.measure(qdev)
                patches.append(measurement.view(bsz, 4))
        return torch.cat(patches, dim=1)

class QuanvolutionQuantumNet(tq.QuantumModule):
    """
    Fully quantum network that implements the quanvolution filter.  The output
    is a flat feature vector that can be used directly by a classical head
    or as a quantum representation of the image.
    """
    def __init__(self):
        super().__init__()
        self.qfilter = QuanvolutionFilter()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.qfilter(x)

__all__ = ["QuanvolutionQuantumNet"]
