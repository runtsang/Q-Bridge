# Wrapper that mirrors the classical model but uses the quantum cell
# --------------------------------------------------------------------------- #

class QuantumHybridQLSTM(nn.Module):
    """Hybrid LSTM that uses a quantum cell and graph‑regularised hidden states."""
    def __init__(self, embedding_dim, hidden_dim, vocab_size, tagset_size, n_qubits):
        super().__init__()
        self.word_embeddings = nn.Embedding(vocab_size, embedding_dim)
        self.lstm = QLSTM(embedding_dim, hidden_dim, n_qubits)
        self.hidden2tag = nn.Linear(hidden_dim, tagset_size)

    def forward(self, sentence):
        embeds = self.word_embeddings(sentence).unsqueeze(1)
        lstm_out, _ = self.lstm(embeds)
        tag_logits = self.hidden2tag(lstm_out.squeeze(1))
        return torch.nn.functional.log_softmax(tag_logits, dim=1)

    def build_adjacency(self, hidden_states, threshold=0.9):
        return fidelity_adjacency(hidden_states, threshold)

    def smooth_hidden(self, hidden_states, adj):
        row_sums = adj.sum(dim=1, keepdim=True).clamp_min(1.0)
        norm_adj = adj / row_sums
        return torch.matmul(norm_adj, hidden_states)

