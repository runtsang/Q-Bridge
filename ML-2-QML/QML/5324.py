"""Hybrid quantum LSTM module combining quantum circuits, graph utilities, and fast estimators.

The module defines a :class:`HybridQLSTM` that mirrors the classical implementation
but uses the quantum LSTM from the seed.  It also exposes graph‑based adjacency
construction on quantum states and a fast estimator that works directly on the
quantum circuit.  The API is intentionally compatible with the seed while
adding new quantum‑centric features.
"""

