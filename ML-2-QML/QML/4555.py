# Unified hybrid transformer classifier – quantum‑aware
# --------------------------------------------------------------------------- #
class HybridTransformerClassifier(nn.Module):
    def __init__(
        self,
        vocab_size: int | None = None,
        embed_dim: int = 128,
        num_heads: int = 8,
        num_blocks: int = 4,
        ffn_dim: int = 512,
        num_classes: int = 10,
        dropout: float = 0.1,
        use_quanvolution: bool = False,
        use_fraud_layer: bool = False,
        fraud_params: Sequence[FraudLayerParameters] | None = None,
        use_quantum: bool = False,
        n_qubits_transformer: int = 0,
        n_qubits_ffn: int = 0,
    ) -> None:
        super().__init__()
        self.use_quanvolution = use_quanvolution
        self.use_fraud_layer = use_fraud_layer
        self.use_quantum = use_quantum

        if use_quanvolution:
            self.qfilter = QuanvolutionFilter()
            self.feature_mapper = nn.Linear(4 * 14 * 14, embed_dim)
        else:
            if vocab_size is None:
                raise ValueError("vocab_size must be provided if not using quanvolution")
            self.token_embedding = nn.Embedding(vocab_size, embed_dim)

        self.pos_encoder = PositionalEncoder(embed_dim)

        if use_quantum:
            self.transformer_blocks = nn.ModuleList(
                [TransformerBlockQuantum(embed_dim, num_heads, ffn_dim,
                                         n_qubits_transformer, n_qubits_ffn,
                                         q_device=None, dropout=dropout)
                 for _ in range(num_blocks)]
            )
        else:
            self.transformer_blocks = nn.ModuleList(
                [TransformerBlockClassical(embed_dim, num_heads, ffn_dim, dropout)
                 for _ in range(num_blocks)]
            )

        if use_fraud_layer:
            if fraud_params is None:
                raise ValueError("fraud_params must be provided when use_fraud_layer is True")
            self.fraud_module = build_fraud_detection_program(fraud_params[0], fraud_params[1:])
            self.classifier = nn.Linear(ffn_dim, num_classes)
        else:
            self.classifier = nn.Linear(embed_dim, num_classes)

        self.dropout = nn.Dropout(dropout)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        if self.use_quanvolution:
            features = self.qfilter(x)
            x = self.feature_mapper(features).view(x.size(0), -1, self.feature_mapper.out_features)
        else:
            x = self.token_embedding(x)

        x = self.pos_encoder(x)

        for block in self.transformer_blocks:
            x = block(x)

        pooled = x.mean(dim=1)

        if self.use_fraud_layer:
            pooled = self.fraud_module(pooled)

        pooled = self.dropout(pooled)
        return self.classifier(pooled)


