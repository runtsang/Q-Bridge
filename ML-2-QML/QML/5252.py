def estimator_qnn() -> QEstimatorQNN:
    """Return a Qiskit EstimatorQNN instance with a single‑qubit circuit."""
    params = [Parameter("input1"), Parameter("weight1")]
    qc = QuantumCircuit(1)
    qc.h(0)
    qc.ry(params[0], 0)
    qc.rx(params[1], 0)
    observable = SparsePauliOp.from_list([("Y" * qc.num_qubits, 1)])

    estimator = StatevectorEstimator()
    return QEstimatorQNN(
        circuit=qc,
        observables=observable,
        input_params=[params[0]],
        weight_params=[params[1]],
        estimator=estimator,
    )

