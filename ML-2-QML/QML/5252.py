"""Combined quantum Graph Neural Network utilities with auxiliary modules.

The quantum side mirrors the classical helpers but operates on Qobj
states and Qiskit circuits.  The public API is intentionally
identical to the classical module so that experiments can be swapped
between paradigms with minimal code changes.
"""

