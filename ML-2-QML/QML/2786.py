class HybridTransformer(nn.Module):
    """Hybrid transformer / LSTM model with optional quantum sub‑modules."""
    def __init__(
        self,
        vocab_size: int,
        embed_dim: int,
        num_heads: int,
        num_blocks: int,
        ffn_dim: int,
        num_classes: int,
        dropout: float = 0.1,
        n_qubits_attention: int = 8,
        n_qubits_ffn: int = 8,
        use_lstm: bool = False,
        lstm_hidden_dim: Optional[int] = None,
    ) -> None:
        super().__init__()
        self.use_lstm = use_lstm
        if use_lstm:
            hidden_dim = lstm_hidden_dim or embed_dim
            self._model = LSTMTagger(
                embedding_dim=embed_dim,
                hidden_dim=hidden_dim,
                vocab_size=vocab_size,
                tagset_size=num_classes,
                n_qubits=n_qubits_attention,
            )
        else:
            self._model = TextClassifier(
                vocab_size=vocab_size,
                embed_dim=embed_dim,
                num_heads=num_heads,
                num_blocks=num_blocks,
                ffn_dim=ffn_dim,
                num_classes=num_classes,
                dropout=dropout,
                n_qubits_attention=n_qubits_attention,
                n_qubits_ffn=n_qubits_ffn,
            )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self._model(x)


