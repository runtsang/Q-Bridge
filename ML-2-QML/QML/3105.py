"""Hybrid quantum regression model with a quantum attention layer.

The quantum model follows the same data generation logic as the
classical counterpart but encodes the input into a quantum state,
applies a variational layer, and then uses an additional quantum
attention circuit before measurement.  The attention circuit consists
of a random layer followed by trainable RX/RY gates, producing a
weight vector that is multiplied with the measurement results.
"""

from __future__ import annotations

import numpy as np
import torch
import torch.nn as nn
import torchquantum as tq

def generate_superposition_data(num_wires: int, samples: int) -> tuple[np.ndarray, np.ndarray]:
    """Generate synthetic regression data using a polar‑angle superposition.

    The returned states are complex vectors of size ``2**num_wires``.
    Labels are ``sin(2θ) * cos(φ)`` where θ and φ are random angles.
    """
    omega0 = np.zeros(2 ** num_wires, dtype=complex)
    omega0[0] = 1.0
    omega1 = np.zeros(2 ** num_wires, dtype=complex)
    omega1[-1] = 1.0

    thetas = 2 * np.pi * np.random.rand(samples)
    phis = 2 * np.pi * np.random.rand(samples)
    states = np.zeros((samples, 2 ** num_wires), dtype=complex)
    for i in range(samples):
        states[i] = np.cos(thetas[i]) * omega0 + np.exp(1j * phis[i]) * np.sin(thetas[i]) * omega1
    labels = np.sin(2 * thetas) * np.cos(phis)
    return states.astype(np.complex64), labels.astype(np.float32)

class RegressionDataset(torch.utils.data.Dataset):
    """Dataset wrapping the synthetic quantum data."""
    def __init__(self, samples: int, num_wires: int):
        self.states, self.labels = generate_superposition_data(num_wires, samples)

    def __len__(self) -> int:  # type: ignore[override]
        return len(self.states)

    def __getitem__(self, index: int):  # type: ignore[override]
        return {
            "states": torch.tensor(self.states[index], dtype=torch.cfloat),
            "target": torch.tensor(self.labels[index], dtype=torch.float32),
        }

class QModel(tq.QuantumModule):
    """Quantum regression circuit with a quantum attention layer."""
    class BaseLayer(tq.QuantumModule):
        """Variational layer producing feature‑rich quantum state."""
        def __init__(self, num_wires: int):
            super().__init__()
            self.n_wires = num_wires
            self.random_layer = tq.RandomLayer(n_ops=30, wires=list(range(num_wires)))
            self.rx = tq.RX(has_params=True, trainable=True)
            self.ry = tq.RY(has_params=True, trainable=True)

        def forward(self, qdev: tq.QuantumDevice):
            self.random_layer(qdev)
            for wire in range(self.n_wires):
                self.rx(qdev, wires=wire)
                self.ry(qdev, wires=wire)

    class AttentionLayer(tq.QuantumModule):
        """Quantum attention circuit that outputs a weight vector."""
        def __init__(self, num_wires: int):
            super().__init__()
            self.n_wires = num_wires
            # A smaller random layer to generate attention weights
            self.random_layer = tq.RandomLayer(n_ops=10, wires=list(range(num_wires)))
            self.rx = tq.RX(has_params=True, trainable=True)
            self.ry = tq.RY(has_params=True, trainable=True)

        def forward(self, qdev: tq.QuantumDevice):
            self.random_layer(qdev)
            for wire in range(self.n_wires):
                self.rx(qdev, wires=wire)
                self.ry(qdev, wires=wire)

    def __init__(self, num_wires: int):
        super().__init__()
        self.n_wires = num_wires
        self.encoder = tq.GeneralEncoder(
            tq.encoder_op_list_name_dict[f"{num_wires}xRy"]
        )
        self.base_layer = self.BaseLayer(num_wires)
        self.attention_layer = self.AttentionLayer(num_wires)
        self.measure = tq.MeasureAll(tq.PauliZ)
        self.head = nn.Linear(num_wires, 1)

    def forward(self, state_batch: torch.Tensor) -> torch.Tensor:
        bsz = state_batch.shape[0]
        qdev = tq.QuantumDevice(n_wires=self.n_wires, bsz=bsz, device=state_batch.device)
        self.encoder(qdev, state_batch)
        self.base_layer(qdev)
        # Attention layer modifies the state before measurement
        self.attention_layer(qdev)
        features = self.measure(qdev)
        return self.head(features).squeeze(-1)

__all__ = ["QModel", "RegressionDataset", "generate_superposition_data"]
