"""FastBaseEstimator__gen082.py – Classical estimator and model utilities.

Features
--------
* Lightweight batch‑wise evaluation of neural‑networks with arbitrary scalar observables.
* Optional Gaussian shot‑noise emulation.
* Factory helpers for autoencoders and simple classifiers.
* All code is pure PyTorch – no external dependencies beyond numpy and torch.

Classes
-------
FastBaseEstimator
    Core evaluator for a torch.nn.Module.
FastEstimator
    Subclass adding shot‑noise control.

Functions
---------
Autoencoder
    Factory that returns a fully‑connected autoencoder.
train_autoencoder
    Simple reconstruction training loop.
build_classifier_circuit
    Builds a feed‑forward classifier with metadata mirroring the quantum API.
"""

from __future__ import annotations

from collections.abc import Iterable, Sequence
from dataclasses import dataclass
from typing import Callable, List

import numpy as np
import torch
from torch import nn

ScalarObservable = Callable[[torch.Tensor], torch.Tensor | float]


def _ensure_batch(values: Sequence[float]) -> torch.Tensor:
    """Convert a 1‑D parameter list to a 2‑D batch tensor."""
    tensor = torch.as_tensor(values, dtype=torch.float32)
    if tensor.ndim == 1:
        tensor = tensor.unsqueeze(0)
    return tensor


class FastBaseEstimator:
    """Evaluate a torch.nn.Module for batches of parameters and observables."""

    def __init__(self, model: nn.Module) -> None:
        self.model = model

    def evaluate(
        self,
        observables: Iterable[ScalarObservable],
        parameter_sets: Sequence[Sequence[float]],
    ) -> List[List[float]]:
        """Return a 2‑D list of scalar results.

        Parameters
        ----------
        observables
            Iterable of callables that map a model output to a scalar.
        parameter_sets
            Iterable of parameter sequences; each is passed as a batch to the model.
        """
        observables = list(observables) or [lambda outputs: outputs.mean(dim=-1)]
        results: List[List[float]] = []
        self.model.eval()
        with torch.no_grad():
            for params in parameter_sets:
                inputs = _ensure_batch(params)
                outputs = self.model(inputs)
                row: List[float] = []
                for observable in observables:
                    value = observable(outputs)
                    if isinstance(value, torch.Tensor):
                        scalar = float(value.mean().cpu())
                    else:
                        scalar = float(value)
                    row.append(scalar)
                results.append(row)
        return results


class FastEstimator(FastBaseEstimator):
    """Adds optional Gaussian shot‑noise to the deterministic output."""

    def evaluate(
        self,
        observables: Iterable[ScalarObservable],
        parameter_sets: Sequence[Sequence[float]],
        *,
        shots: int | None = None,
        seed: int | None = None,
    ) -> List[List[float]]:
        raw = super().evaluate(observables, parameter_sets)
        if shots is None:
            return raw
        rng = np.random.default_rng(seed)
        noisy: List[List[float]] = []
        for row in raw:
            noisy_row = [float(rng.normal(mean, max(1e-6, 1 / shots))) for mean in row]
            noisy.append(noisy_row)
        return noisy


