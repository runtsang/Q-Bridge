import torch
import torch.nn as nn
import torch.nn.functional as F

class HybridQuantumClassifier(nn.Module):
    """Pure‑PyTorch baseline that mirrors the hybrid architecture.

    It replaces the quantum expectation head with a trainable linear
    layer, enabling quick ablations and serving as a reference point
    for the quantum‑augmented model.
    """
    def __init__(self):
        super().__init__()
        self.conv1 = nn.Conv2d(3, 6, kernel_size=5, stride=2, padding=1)
        self.conv2 = nn.Conv2d(6, 15, kernel_size=3, stride=2, padding=1)
        self.pool = nn.MaxPool2d(kernel_size=2, stride=1)
        self.drop1 = nn.Dropout2d(p=0.2)
        self.drop2 = nn.Dropout2d(p=0.5)
        self.fc1 = nn.Linear(55815, 120)
        self.fc2 = nn.Linear(120, 84)
        self.fc3 = nn.Linear(84, 1)
        # Trainable head that mimics the quantum expectation
        self.classifier = nn.Linear(1, 2)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = F.relu(self.conv1(x))
        x = self.pool(x)
        x = self.drop1(x)
        x = F.relu(self.conv2(x))
        x = self.pool(x)
        x = self.drop1(x)
        x = torch.flatten(x, 1)
        x = F.relu(self.fc1(x))
        x = self.drop2(x)
        x = F.relu(self.fc2(x))
        x = self.fc3(x)
        logits = self.classifier(x)
        probs = F.softmax(logits, dim=-1)
        return probs
